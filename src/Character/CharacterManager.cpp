#include "CharacterManager.h"

//コンストラクタ
CCharacterManager::CCharacterManager()
{
	//メインIDを初期化
	MainID = ID_NUM;
}

//デストラクタ
CCharacterManager::~CCharacterManager()
{
}


//初期化
void CCharacterManager::Init()
{
	cAttacker.Init();
	//メインIDを初期化
	MainID = ID_NUM;
}

//データロード
void CCharacterManager::Load()
{
	cAttacker.Load();
}

//終了処理
void CCharacterManager::Exit()
{
	//メインキャラ後処理
	switch (MainID)
	{
	case MainID_ATTACKER:
		cAttacker.Exit();
		break;

	case MainID_TANK:
		cTank.Exit();
		break;

	case MainID_SUPPORT:
		cSupport.Exit();
		break;

	case ID_NUM:
		break;

	default:
		break;
	}
}

//毎フレーム呼ぶ処理
void CCharacterManager::Step()
{
	//メインキャラ処理
	switch (MainID)
	{
	case MainID_ATTACKER:
		cAttacker.Step();
		cAttacker.Update();
		break;

	case MainID_TANK:
		cTank.Step();
		cTank.Update();
		break;

	case MainID_SUPPORT:
		cSupport.Step();
		cSupport.Update();
		break;

	case ID_NUM:
		break;

	default:
		break;
	}	
}

//描画処理
void CCharacterManager::Draw()
{
	//メインキャラ描画
	switch (MainID)
	{
	case MainID_ATTACKER:
		cAttacker.Draw();
		break;

	case MainID_TANK:
		cTank.Draw();
		break;

	case MainID_SUPPORT:
		cSupport.Draw();
		break;

	case ID_NUM:
		break;

	default:
		break;
	}
}